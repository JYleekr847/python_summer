#지수, 로그 연산
import math
a = math.pow(2,10)
print(a) # 2^10

b = math.pow(1.5 ,3 )
print(b)

c = math.sqrt(2) # 2의 제곱급
print(c)

d = math.exp(2) # 자연수 e 의 제곱(2)
print(d)

# 삼각함수 연산

r = math.radians(30)
print(math.sin(r))

r = math.radians(180)
v = math.cos(r)
print(v)
r == math.acos(v)
print(math.degrees(r))
